import Foundation
import UIKit

extension UIColor {
  
  static func flatYellow() -> UIColor {
    return UIColor(colorLiteralRed: 0.945, green: 0.768, blue: 0.0588, alpha: 1.0)
  }
  
  static func flatLightGreen() -> UIColor {
    return UIColor(colorLiteralRed: 0.18, green: 0.8, blue: 0.443, alpha: 1.0)
  }
  
  static func flatDarkGreen() -> UIColor {
    return UIColor(colorLiteralRed: 0.153, green: 0.68, blue: 0.376, alpha: 1.0)
  }
  
  static func flatLightOrange() -> UIColor {
    return UIColor(colorLiteralRed: 0.95, green: 0.611, blue: 0.071, alpha: 1.0)
  }
  
  static func flatDarkOrange() -> UIColor {
    return UIColor(colorLiteralRed: 0.90, green: 0.49, blue: 0.13, alpha: 1.0)
  }
  
  static func flatLightGrey() -> UIColor {
    return UIColor(colorLiteralRed: 0.925, green: 0.941, blue: 0.945, alpha: 1.0)
  }
}
